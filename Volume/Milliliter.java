package Volume;

public class Milliliter {

   public double milliliterToLiter(double ml) {
      return ml / 1000;

   }

   public double milliliterToCubicFoot(double ml) {
      return ml / 28320;
   }

   public double milliliterToUsLiquidGallon(double ml) {
      return ml / 3785;
   }

   public double milliliterToCubicInch(double ml) {
      return ml / 16.387;
   }

}
