public class Time {

    public double hourToMinute(double hour){
        return hour * 60;
    }

    public double minuteToHour(double minute){
        return minute / 60;
    }

    public double minuteToSecond(double minute){
        return minute * 60;
    }

    public double secondToMinute(double second){
        return second / 60;
    }


}
