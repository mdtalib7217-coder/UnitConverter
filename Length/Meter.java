package Length;

public class Meter {
    public double meterToKilometer(double meter) {
        return meter / 1000;
    }

    public double meterToCentimeter(double meter) {
        return meter * 100;
    }

    public double meterToMillimeter(double meter) {
        return meter * 1000;
    }

    public double meterToMilimeter(double m) {
        return m * 1000;
    }
}
