public class Volume {

    public double literToMilliliter(double liter){
        return liter * 1000;
    }

    public double milliliterToLiter(double ml){
        return ml / 1000;
    }

    public double literToCubicMeter(double liter){
        return liter /  1000;
    }

    public double cubicMeterToLiter(double cubicMeter){
        return cubicMeter * 1000;
    }
    
}
